str1 = "Hello"
str2 = "World"

# Copying a string
str3 = str1
print("Copied string:", str3)

# Concatenating strings
str4 = str1 + " " + str2
print("Concatenated string:", str4)

# Manipulating strings (upper case)
str5 = str1.upper()
print("Upper case string:", str5)

# Manipulating strings (lower case)
str6 = str2.lower()
print("Lower case string:", str6)